import Foundation

func runAfter(_ delay: Double, queue: DispatchQueue = DispatchQueue.main, closure: @escaping () -> Void) {
    queue.asyncAfter(
        deadline: DispatchTime.now() + Double(Int64(delay * Double(NSEC_PER_SEC))) / Double(NSEC_PER_SEC), execute: closure)
}
